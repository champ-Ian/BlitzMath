function typing(){
    if (sessionStart==false){
        sessionStart=true
        start()
    }
    if (document.querySelector('.input').value == questionAnswer){
        score+=1;
        document.querySelector('.input').value = ''
        loadQuestion()
    }
}

function start(){
    document.querySelector('.settingButton').disabled = true
    let sec = findTime(time)
    timer = setInterval(()=>{
        if (sec >= 120){
            document.querySelector('.timer').innerHTML = '02:0' + (sec-120);
        }
        else if (sec >= 70){
            document.querySelector('.timer').innerHTML = '01:' + (sec-60);
        } else if (sec >= 60){
            document.querySelector('.timer').innerHTML = '01:0' + (sec-60);
        } else if (sec >= 10){
            document.querySelector('.timer').innerHTML = '00:' + (sec);
        } else{
            document.querySelector('.timer').innerHTML = '00:0' + (sec);
        }
        sec --;
        if (sec < 0){
            clearInterval(timer);
            end()
        }
    }, 1000)

}

function restart(){
    if (sessionStart==true){
        clearInterval(timer);
        testReset();
    } 
}

function findTime(time){
    if (time == 1){
        return 15
    } else if (time == 2){
        return 30
    } else if (time == 3){
        return 60
    } else {
        return 120
    }
}

function findMode(mode){
    if(mode==1){
        return 'Combined'
    } else if (mode==2){
        return 'Addition'
    } else if (mode ==3){
        return 'Subtraction'
    } else if (mode ==4){
        return 'Multiplication'
    } else{
        return 'Division'
    }
}

function end(){
    let qpm = Math.round(score*(60/findTime(time)))

    document.querySelector('.testEnd').style.display = 'flex'
    document.querySelector('.finalMessage').innerHTML = generateMessage(qpm)
    document.querySelector('.finalScore').innerHTML = score
    document.querySelector('.finalQpm').innerHTML = qpm
    document.querySelector('.finalMode').innerHTML = findMode(mode)
    document.querySelector('.finalTime').innerHTML = findTime(time)
}

function testReset(){
    sessionStart = false;
    questionAnswer = null;
    score = 0;
    document.querySelector('.testEnd').style.display = 'none'
    document.querySelector('.settingButton').disabled = false
    document.querySelector('.input').value = ''
    timeSelect(time)
    modeSelect(mode)

    loadQuestion()
}

function generateMessage(qpm){
    if (qpm < 20){
        return 'Oof, you can do better.'
    } else if (qpm < 30){
        return "Let's keep working on it."
    } else if (qpm < 45){
        return 'Hmm, we can go faster.'
    } else if (qpm < 55){
        return 'Not bad, but you can do better.'
    } else if (qpm < 60){
        return 'Ok! You know your stuff!'
    } else if (qpm < 75){
        return 'Your pretty fast, keep on going to the next level!'
    } else if (qpm < 85){
        return 'Nice, your really good at this!'
    } else if (qpm < 95){
        return 'Wow, only two more levels to go!'
    } else if (qpm < 100){
        return 'Crazy, your amazing at this, one more level to go!'
    } else {
        return 'Unbelievable! Are you even human?'
    }
}